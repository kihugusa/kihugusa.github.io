# 1) Items in a list

lst1 = ["apples", "banana", "pear","grapefruit","pineapple",\
        "grape","guava","plum","peach"]

print(lst1[-1]) # last item in list
print(lst1[0])  # first item in list
print(lst1[:(len(lst1)-3)])  # all but last three
print(lst1[0::2])  # first, third, fifth etc.
print(lst1[1::2])  # second, fourth, sixth etc.

